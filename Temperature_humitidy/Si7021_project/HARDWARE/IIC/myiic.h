//IO方向设置
#define SDA_IN()  {IIC_GPIO->CRH&=0XFFFFFF0F;IIC_GPIO->CRH|=8<<4;}
#define SDA_OUT() {IIC_GPIO->CRH&=0XFFFFFF0F;IIC_GPIO->CRH|=3<<4;}

